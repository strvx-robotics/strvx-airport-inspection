export function regionFromLocation(location: string): string | null {
  const part = location.split(", ").at(-1)?.trim();
  return part ? part.toUpperCase() : null;
}

export function isAmericanRegion(region: string): boolean {
  return US_REGION_CODES.has(region.toUpperCase());
}
